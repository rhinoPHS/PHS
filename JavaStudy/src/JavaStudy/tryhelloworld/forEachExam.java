package JavaStudy.tryhelloworld;

public class forEachExam {
	public static void main(String[] args) {
		
		 int[] iarr = {10,20,30,40,50};

		    for(int value:iarr){
		        System.out.println(value);
		        
		    for(int value1:iarr)
		    {
		    	System.out.println(value1);
		    }
		  }
		
	}

}
